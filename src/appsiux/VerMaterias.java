    private void buttonModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonModificarActionPerformed
       seleccion = listMateria.getSelectedIndex();
       if(seleccion != -1){
       String[] options = {"Nombre", "ID"};
       int selec = JOptionPane.showOptionDialog(null, "Seleccione el atributo a modificar",
       "Modificar", JOptionPane.DEFAULT_OPTION, JOptionPane.QUESTION_MESSAGE,null, options, options[0]);
   
       MateriasModel.actualizarMateria(seleccion, selec);
        }else{
        JOptionPane.showMessageDialog(null, "Selecciona una materia", "Modificar datos", JOptionPane.WARNING_MESSAGE);
       }
        
    }//GEN-LAST:event_buttonModificarActionPerformed

