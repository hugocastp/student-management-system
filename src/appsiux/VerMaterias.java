    int contador = 0;
    public void verificar(){
        if(txtNombreMateria.getText().isEmpty()){obligatorio1.setVisible(true);contador++;}else{
        obligatorio1.setVisible(false);}
         if(txtIdMateria.getText().isEmpty()){obligatorio2.setVisible(true);contador++;}else{
        obligatorio2.setVisible(false);}
        String validarId = txtIdMateria.getText();
    for (Materia m : MateriasModel.getListMateria()) {
        if (m.getId().equals(validarId)) {
             contador++;
            JOptionPane.showMessageDialog(null, "El ID ingresado ya est√° en uso", "Error",ERROR_MESSAGE);
           break;
        }
    }
    }

